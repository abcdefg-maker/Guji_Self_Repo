namespace TimeSystem
{
    /// <summary>
    /// 时段枚举
    /// </summary>
    public enum DayPhase
    {
        Dawn = 0,       // 黎明 (5:00 - 7:00)
        Morning = 1,    // 上午 (7:00 - 12:00)
        Afternoon = 2,  // 下午 (12:00 - 17:00)
        Evening = 3,    // 傍晚 (17:00 - 20:00)
        Night = 4       // 夜晚 (20:00 - 5:00)
    }
}
